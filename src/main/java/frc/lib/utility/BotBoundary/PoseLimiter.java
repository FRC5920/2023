package frc.lib.utility.BotBoundary;

import edu.wpi.first.math.geometry.Pose2d;
import java.util.function.Supplier;

/**
 * PoseLimiter provides a class that monitors the robot pose to determine its relation to given
 * boundaries
 */
public class PoseLimiter {
  private final Supplier<Pose2d> m_poseSupplier;
  private PoseBoundary m_poseBoundary;
  private BoundaryPolicy m_policy;

  /**
   * Creates an object that obtains its pose from a given supplier and relates it to a given
   * boundary
   *
   * @param poseSupplier Supplier used to access the robot's current pose
   * @param boundary Boundary to maintain
   * @param boundaryPolicy Boundary policy to be enforced
   */
  public PoseLimiter(Supplier<Pose2d> poseSupplier, PoseBoundary boundary, BoundaryPolicy policy) {
    m_poseSupplier = poseSupplier;
    m_poseBoundary = boundary;
    m_policy = policy;
  }

  /** Returns true if the current pose is being limited */
  public boolean shouldLimitPose() {
    Pose2d currentPose = m_poseSupplier.get();
    boolean shouldLimit = false;
    switch (m_policy) {
      case KeepInside:
        shouldLimit = m_poseBoundary.isOutsideBoundary(currentPose);
        break;
      case KeepOutside:
        shouldLimit = m_poseBoundary.isInsideBoundary(currentPose);
        break;
    }
    return shouldLimit;
  }

  public enum BoundaryPolicy {
    /** The pose limiter will work to keep the bot inside the boundary */
    KeepInside,
    /** The pose limiter will work to keep the bot outside the boundary */
    KeepOutside;
  }
}
