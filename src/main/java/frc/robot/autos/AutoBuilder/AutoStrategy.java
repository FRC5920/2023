package frc.robot.autos.AutoBuilder;

import com.pathplanner.lib.PathPlannerTrajectory;
import edu.wpi.first.math.geometry.Pose2d;
import edu.wpi.first.wpilibj2.command.CommandBase;
import java.util.ArrayList;
import java.util.List;
import java.util.function.Supplier;

/** IAutoStrategy provides a common interface for autonomous strategies */
public abstract class AutoStrategy {
  /** Supplier of the initial pose used by the strategy */
  protected final Supplier<Pose2d> m_initialPoseSupplier;

  /** Creates the strategy an sets up its initial pose supplier */
  protected AutoStrategy(Supplier<Pose2d> initialPoseSupplier) {
    m_initialPoseSupplier = initialPoseSupplier;
  }

  /** Returns a Command that executes the strategy */
  public abstract CommandBase getCommand();

  /** Returns a list of Trajectories used to display the path(s) traveled by the strategy */
  public List<PathPlannerTrajectory> getTrajectories() {
    return new ArrayList<PathPlannerTrajectory>();
  }

  /** Returns the final pose after the strategy has executed */
  public abstract Pose2d getFinalPose();
}
