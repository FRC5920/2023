package frc.robot.subsystems.SwerveDrivebase;

import com.ctre.phoenix.sensors.WPI_Pigeon2;
import edu.wpi.first.math.geometry.Pose2d;
import edu.wpi.first.math.geometry.Rotation2d;
import edu.wpi.first.math.geometry.Translation2d;
import edu.wpi.first.math.kinematics.ChassisSpeeds;
import edu.wpi.first.math.kinematics.SwerveDriveKinematics;
import edu.wpi.first.math.kinematics.SwerveDriveOdometry;
import edu.wpi.first.math.kinematics.SwerveModulePosition;
import edu.wpi.first.math.kinematics.SwerveModuleState;
import edu.wpi.first.wpilibj.Timer;
import edu.wpi.first.wpilibj.smartdashboard.SmartDashboard;
import edu.wpi.first.wpilibj2.command.SubsystemBase;
import frc.lib.SwerveDrive.SwerveModule;
import frc.lib.SwerveDrive.SwerveModuleIO;
import frc.robot.Constants;

public class Swerve extends SubsystemBase {

  /** Swerve module ID's */
  public enum ModuleId {
    kFrontLeft(0),
    kFrontRight(1),
    kRearLeft(2),
    kRearRight(3);

    public final int value;

    private ModuleId(int id) {
      value = id;
    }

    /**
     * @return the human-readable name of the module ID
     */
    @Override
    public String toString() {
      return this.name().substring(1); // Strip 'k' prefix from name
    }
  };

  public SwerveDriveOdometry swerveOdometry;
  public SwerveModule[] mSwerveMods;
  public WPI_Pigeon2 gyro;

  public Swerve(
      SwerveModuleIO frontLeftIO,
      SwerveModuleIO frontRightIO,
      SwerveModuleIO rearLeftIO,
      SwerveModuleIO rearRightIO) {
    gyro = new WPI_Pigeon2(Constants.SwerveDrivebaseConstants.pigeonID, "SwerveCAN");
    gyro.configFactoryDefault();
    zeroGyro();

    mSwerveMods =
        new SwerveModule[] {
          new SwerveModule(0, frontLeftIO),
          new SwerveModule(1, frontRightIO),
          new SwerveModule(2, rearLeftIO),
          new SwerveModule(3, rearRightIO)
        };

    /* By pausing init for a second before setting module offsets, we avoid a bug with inverting motors.
     * See https://github.com/Team364/BaseFalconSwerve/issues/8 for more info.
     */
    Timer.delay(1.0);
    resetModulesToAbsolute();

    swerveOdometry =
        new SwerveDriveOdometry(
            Constants.SwerveDrivebaseConstants.swerveKinematics, getYaw(), getModulePositions());
  }

  public void drive(
      Translation2d translation, double rotation, boolean fieldRelative, boolean isOpenLoop) {
    SwerveModuleState[] swerveModuleStates =
        Constants.SwerveDrivebaseConstants.swerveKinematics.toSwerveModuleStates(
            fieldRelative
                ? ChassisSpeeds.fromFieldRelativeSpeeds(
                    translation.getX(), translation.getY(), rotation, getYaw())
                : new ChassisSpeeds(translation.getX(), translation.getY(), rotation));
    SwerveDriveKinematics.desaturateWheelSpeeds(
        swerveModuleStates, Constants.SwerveDrivebaseConstants.maxSpeed);

    for (SwerveModule mod : mSwerveMods) {
      mod.setDesiredState(swerveModuleStates[mod.moduleNumber], isOpenLoop);
    }
  }

  public void stop() {
    drive(new Translation2d(0, 0).times(0), 0, true, true);
  }
  /* Used by SwerveControllerCommand in Auto */
  public void setModuleStates(SwerveModuleState[] desiredStates) {
    SwerveDriveKinematics.desaturateWheelSpeeds(
        desiredStates, Constants.SwerveDrivebaseConstants.maxSpeed);

    for (SwerveModule mod : mSwerveMods) {
      mod.setDesiredState(desiredStates[mod.moduleNumber], false);
    }
  }

  public Pose2d getPose() {
    return swerveOdometry.getPoseMeters();
  }

  public void resetOdometry(Pose2d pose) {
    swerveOdometry.resetPosition(getYaw(), getModulePositions(), pose);
  }

  public SwerveModuleState[] getModuleStates() {
    SwerveModuleState[] states = new SwerveModuleState[4];
    for (SwerveModule mod : mSwerveMods) {
      states[mod.moduleNumber] = mod.getState();
    }
    return states;
  }

  public SwerveModulePosition[] getModulePositions() {
    SwerveModulePosition[] positions = new SwerveModulePosition[4];
    for (SwerveModule mod : mSwerveMods) {
      positions[mod.moduleNumber] = mod.getPosition();
    }
    return positions;
  }

  public void zeroGyro() {
    gyro.setYaw(0);
  }

  public Rotation2d getYaw() {
    return (Constants.SwerveDrivebaseConstants.invertGyro)
        ? Rotation2d.fromDegrees(360 - gyro.getYaw())
        : Rotation2d.fromDegrees(gyro.getYaw());
  }

  public double getRoll() {
    return gyro.getRoll();
  }

  public double getPitch() {
    return gyro.getPitch();
  }

  public Rotation2d getGyroscopeRotation() {
    return gyro.getRotation2d();
  }

  public void resetModulesToAbsolute() {
    for (SwerveModule mod : mSwerveMods) {
      mod.resetToAbsolute();
    }
  }

  @Override
  public void periodic() {
    for (SwerveModule module : mSwerveMods) {
      module.updateLoggedInputs();
    }

    swerveOdometry.update(getYaw(), getModulePositions());

    SmartDashboard.putNumber("Gyro", gyro.getYaw());
    SmartDashboard.putNumber("Roll", getRoll());
    SmartDashboard.putNumber("Pitch", getPitch());
    for (SwerveModule mod : mSwerveMods) {
      SmartDashboard.putNumber(
          "Mod " + mod.moduleNumber + " Cancoder", mod.getAngle().getDegrees());
      SmartDashboard.putNumber(
          "Mod " + mod.moduleNumber + " Integrated", mod.getPosition().angle.getDegrees());
      SmartDashboard.putNumber(
          "Mod " + mod.moduleNumber + " Velocity", mod.getState().speedMetersPerSecond);
    }
  }

  public SwerveModuleIO.SwerveModuleIOTelemetry getIOTelemetry(ModuleId module) {
    return mSwerveMods[module.value].getIOTelemetry();
  }
}
